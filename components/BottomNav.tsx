'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';

const navItems = [
  { href: '/', label: 'Home', icon: HomeIcon, iconFilled: HomeIconFilled },
  { href: '/clients', label: 'Clients', icon: UsersIcon, iconFilled: UsersIconFilled },
  { href: '/history', label: 'History', icon: ClockIcon, iconFilled: ClockIconFilled },
];

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white/80 backdrop-blur-lg border-t border-gray-200/50 pb-safe z-40">
      <div className="flex justify-around items-center h-16 max-w-lg mx-auto">
        {navItems.map(({ href, label, icon: Icon, iconFilled: IconFilled }) => {
          const isActive = pathname === href ||
            (href !== '/' && pathname.startsWith(href));

          return (
            <Link
              key={href}
              href={href}
              className={`flex flex-col items-center justify-center min-w-[72px] min-h-[48px] px-3 py-2 rounded-xl transition-all duration-200 ${
                isActive
                  ? 'text-blue-600'
                  : 'text-gray-400 hover:text-gray-600 active:bg-gray-100'
              }`}
            >
              {isActive ? (
                <IconFilled className="w-6 h-6" />
              ) : (
                <Icon className="w-6 h-6" />
              )}
              <span className={`text-xs mt-1 font-medium ${isActive ? '' : 'font-normal'}`}>
                {label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

